package utilidades.calulaPrimos;
import java.util.*;
public class Datos {
	
	/**
	 * toma y convierte a entero el primer parametro pasado
	 * @param args (primer parametro)
	 * @return valor1Int
	 */
	private int getValor1(String[] args) {
		
		int valor1Int = Integer.parseInt(args[0]);
		
		return valor1Int;
	}
	
	/**
	 * toma y convierte a entero el segundo parametro pasado
	 * @param args (segundo parametro)
	 * @return valor2Int
	 */
	private int getValor2(String[] args) {
		
		int valor2Int = Integer.parseInt(args[1]);
		
		return valor2Int;
	}
	
	
	
	
}
